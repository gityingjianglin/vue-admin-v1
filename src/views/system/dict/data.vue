<script>
import { listData, getData, delData, addData, updateData } from "@/api/system/dict/data";
import { listType, getType } from "@/api/system/dict/type";
import Treeselect from "@riophae/vue-treeselect";
import "@riophae/vue-treeselect/dist/vue-treeselect.css";
export default {
  name: "Data",
  dicts: ['sys_normal_disable'],
  components: { Treeselect },
  data() {
    const parentCodeValidate = (rule, value, callback) => {
      console.log(this.form.parentCode)
      if (this.form.parentCode !== undefined) {
        callback()
      } else {callback('上级字典不能为空')}
      
    }
    return {
      // 遮罩层
      loading: true,
      // 是否展开，默认全部折叠
      isExpandAll: false,
      // 选中数组
      ids: [],
      // 非单个禁用
      single: true,
      // 非多个禁用
      multiple: true,
      // 显示搜索条件
      showSearch: true,
      // 总条数
      total: 0,
      // 字典表格数据
      dataList: [],
      // 菜单树选项
      menuOptions: [],
      // 重新渲染表格状态
      refreshTable: true,
      // 默认字典类型
      defaultDictType: "",
      // 弹出层标题
      title: "",
      // 是否显示弹出层
      open: false,
      // 数据标签回显样式
      listClassOptions: [
        {
          value: "default",
          label: "默认"
        },
        {
          value: "primary",
          label: "主要"
        },
        {
          value: "success",
          label: "成功"
        },
        {
          value: "info",
          label: "信息"
        },
        {
          value: "warning",
          label: "警告"
        },
        {
          value: "danger",
          label: "危险"
        }
      ],
      // 类型数据字典
      typeOptions: [],
      // 查询参数
      queryParams: {
        /* pageNum: 1,
        pageSize: 10, */
        dictName: undefined,
        dictType: undefined,
        status: undefined
      },
      // 表单参数
      form: {
        dictCode: undefined,
        dictLabel: undefined,
        dictValue: undefined,
        cssClass: undefined,
        listClass: 'default',
        dictSort: 0,
        status: "0",
        remark: undefined
      },
      // 表单校验
      rules: {
        dictLabel: [
          { required: true, message: "数据标签不能为空", trigger: "blur" }
        ],
        dictValue: [
          { required: true, message: "数据键值不能为空", trigger: "blur" }
        ],
        dictSort: [
          { required: true, message: "数据顺序不能为空", trigger: "blur" }
        ],
        parentCode: [
          { required: true, validator:parentCodeValidate,trigger:"blur"}
        ]
      }
    };
  },
  created() {
    const dictId = this.$route.params && this.$route.params.dictId;
    this.getType(dictId);
    this.getTypeList();
  },
  methods: {
    /** 展开/折叠操作 */
    toggleExpandAll() {
      this.refreshTable = false
      this.isExpandAll = !this.isExpandAll
      console.log(this.isExpandAll)
      this.$nextTick(() => {
        this.refreshTable = true
      });
    },
    /* 多选框选择事件 */
    select (selection, row) {
      console.log(selection, row,'999090');
      /* if (selection.some(el => { return row.id === el.id })) {
        if (row.children) {
          row.children.map(j => {
            this.toggleSelection(j, true)
          })
        }
      } else {
        if (row.children) {
          row.children.map(j => {
            this.toggleSelection(j, false)
          })
        }
      } */
    },
    /** 转换菜单数据结构 */
    normalizer(node) {
      if (node.children && !node.children.length) {
        delete node.children;
      }
      return {
        id: node.dictCode,
        label: node.dictLabel,
        children: node.children
      };
    },
    /** 查询字典类型详细 */
    getType(dictId) {
      getType(dictId).then(response => {
        this.queryParams.dictType = response.data.dictType;
        this.defaultDictType = response.data.dictType;
        this.getList()
      });
    },
    /** 查询字典类型列表 */
    getTypeList() {
      listType().then(response => {
        this.typeOptions = response.rows;
      });
    },
    /** 查询字典数据列表 */
    getList() {
      this.loading = true;
      listData(this.queryParams).then(response => {
        this.dataList = this.handleTree(response.rows, "dictCode", 'parentCode');
        // this.total = response.total;
        this.loading = false;
      });
      listData({dictType:this.queryParams.dictType}).then(response => {
        this.menuOptions = [];
        const dict = { dictCode: 0, dictLabel: '主字典', children: [] };
        dict.children = this.handleTree(response.rows, "dictCode", 'parentCode');
        this.menuOptions.push(dict);
      })
    },
    // 取消按钮
    cancel() {
      this.open = false;
      if (this.title === '修改字典数据') {
        this.reset();
      }
    },
    // 表单重置
    reset() {
      this.form = {
        dictCode: undefined,
        dictLabel: undefined,
        dictValue: undefined,
        cssClass: undefined,
        listClass: 'default',
        dictSort: 0,
        status: "0",
        remark: undefined
      };
      this.resetForm("form");
    },
    /** 搜索按钮操作 */
    handleQuery() {
      // this.queryParams.pageNum = 1;
      this.getList();
    },
    /** 返回按钮操作 */
    handleClose() {
      const obj = { path: "/system/dict" };
      this.$tab.closeOpenPage(obj);
    },
    /** 重置按钮操作 */
    resetQuery() {
      this.resetForm("queryForm");
      this.queryParams.dictType = this.defaultDictType;
      this.handleQuery();
    },
    /** 新增按钮操作 */
    handleAdd() {
      this.open = true;
      this.title = "添加字典数据";
      this.form.dictType = this.queryParams.dictType;
      this.form.parentCode = 0
    },
    // 多选框选中数据
    handleSelectionChange(selection) {
      this.ids = selection.map(item => item.dictCode)
      this.single = selection.length!=1
      this.multiple = !selection.length
    },
    /** 修改按钮操作 */
    handleUpdate(row) {
      this.reset()
      const dictCode = row.dictCode || this.ids
      getData(dictCode).then(response => {
        this.form = response.data;
        this.open = true;
        this.title = "修改字典数据";
      });
    },
    /** 提交按钮 */
    submitForm: function() {
      this.$refs["form"].validate(valid => {
        if (valid) {
          if (this.form.dictCode != undefined) {
            let childDictCodeList = this.searchChildDictIds(this.dataList, this.form.dictCode, 'dictCode', 'parentCode')
            this.form.childNodes = childDictCodeList
            if (this.form.dictCode === this.form.parentCode) {
              this.$modal.msgError('上级字典不允许为自身')
            } else if (childDictCodeList.indexOf(this.form.parentCode) !== -1) {
              this.$modal.msgError('上级字典不允许为子节点')
            } else {
              updateData(this.form).then(response => {
                this.$modal.msgSuccess("修改成功");
                this.open = false;
                this.getList();
                this.reset()
              });
            }
          } else {
            addData(this.form).then(response => {
              this.$modal.msgSuccess("新增成功");
              this.open = false;
              this.getList();
              this.reset()
            });
          }
        }
      });
    },
    /** 删除按钮操作 */
    handleDelete(row) {
      let dictCodes = row.dictCode
      let childDictCodeList = this.searchChildDictIds(this.dataList, row.dictCode, 'dictCode', 'parentCode')
      let tipMessage
      if (childDictCodeList.length > 0) {
        tipMessage = `字典名称为${row.dictLabel}有子节点，删除操作会连同子节点一起删除，是否确认继续？`
        dictCodes = dictCodes + ','  + childDictCodeList.join(',')
      } else {
        tipMessage = `是否确认删除字典为${row.dictLabel}的数据项？`
      }
      this.$modal.confirm(tipMessage).then(function() {
        return delData(dictCodes);
      }).then(() => {
        this.getList();
        this.$modal.msgSuccess("删除成功");
      }).catch(() => {});
    },
    /** 导出按钮操作 */
    handleExport() {
      this.download('system/dict/data/export', {
        ...this.queryParams
      }, `data_${new Date().getTime()}.xlsx`)
    }
  }
};
</script>